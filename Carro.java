public class Carro {
    private String modeloC;
    private String placaC;
    private String proprietarioC;

    public void setModeloC(String modeloC) {
        this.modeloC = modeloC;
    }

    public void setPlacaC(String placaC) {
        this.placaC = placaC;
    }

    public void setProprietarioC(String proprietarioC) {
        this.proprietarioC = proprietarioC;
    }

    public String getModeloC() {
        return modeloC;
    }

    public String getPlacaC() {
        return placaC;
    }

    public String getProprietarioC() {
        return proprietarioC;
    }

}
